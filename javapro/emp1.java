public class emp1 {
    int eno;
    String ename;
    address addr;
    public emp1(int eno, String ename, address addr) {
        this.eno = eno;
        this.ename = ename;
        this.addr = addr;
    }
    public int getEno() {
        return eno;
    }
    public void setEno(int eno) {
        this.eno = eno;
    }
    public String getEname() {
        return ename;
    }
    public void setEname(String ename) {
        this.ename = ename;
    }
    public address getAddr() {
        return addr;
    }
    public void setAddr(address addr) {
        this.addr = addr;
    }
        
}
