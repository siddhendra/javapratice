public class switchwithoutbreak {
    public static void main(String[] args) {
        int i=2;
        switch(i)
        {
            case 1:
            System.out.println("case 1");
            case 2:
            System.out.println("case 2");
            case 3:
            System.out.println("case 3");
            default:
            System.out.println("default");
        }


    }
    
}
