public class switchcharexp {
    public static void main(String[] args) {
        Character c='s';
        switch(c)
        {
            case 'd':
          System.out.println("case1");
          break;
          case 'f':
          System.out.println("case2");
          break;
          case 'e':
          System.out.println("case 3");
          break;
          case 's':
          System.out.println("case 4");
          break;
          default:
          System.out.println("default");
        }
    }
    
}
